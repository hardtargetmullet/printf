#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s) - 1;
	if (s[i + 1] == 0 && c == 0)
		return ((char *)s + i + 1);
	while (s[i] != (char)c)
	{
		if (i < 0)
			return (0);
		i--;
	}
	return ((char *)s + i);
}
