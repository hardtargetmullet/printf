int	ft_atoi(const char *str)
{
	int n;
	int sign;
	int i;

	n = 0;
	i = 0;
	sign = 1;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == ' ' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ((str[i] != '\0') && (str[i] >= '0' && str[i] <= '9'))
	{
		n = n * 10 + str[i] - 48;
		i++;
	}
	return (n * sign);
}
